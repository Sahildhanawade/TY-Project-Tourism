<script>
    const searchInput = document.getElementById("search");
    const suggestionsBox = document.getElementById("suggestions");

    const keywords = {
      "Pune": "Pune.html",
      "Solapur": "Solapur.html",
      "Kolhapur": "Kolhapur.html",
      "Satara": "Satara.html",
      "Ratnagiri": "Ratnagiri.html",
    };

    searchInput.addEventListener("input", () => {
      const input = searchInput.value.toLowerCase();
      suggestionsBox.innerHTML = "";

      if (input === "") return;

      const matches = Object.keys(keywords).filter(item =>
        item.toLowerCase().includes(input)
      );

      matches.forEach(match => {
        const div = document.createElement("div");
        div.textContent = match;
        div.onclick = () => {
          searchInput.value = match;
          suggestionsBox.innerHTML = "";
          window.location.href = keywords[match];
        };
        suggestionsBox.appendChild(div);
      });
    });

    function searchNow() {
      const input = searchInput.value.trim();
      const url = keywords[input];

      if (url) {
        window.location.href = url;
      } else {
        alert("No results found for \"" + input + "\"");
      }
    }

    document.addEventListener("click", (e) => {
      if (!document.querySelector(".search-container").contains(e.target)) {
        suggestionsBox.innerHTML = "";
      }
    });
